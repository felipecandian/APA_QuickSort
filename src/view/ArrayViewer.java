    public void loadArray(String fileName) throws IOException{
        //<editor-fold defaultstate="collapsed" desc="Vars">
        String write = null;
        int count = 0;
        //</editor-fold>
        try{
            FileReader f = new FileReader(fileName + ".txt");
            Scanner lerArq = new Scanner(f);
            lerArq.useDelimiter(",");
            while(lerArq.hasNext()){
                if(write == null){
                    write = lerArq.next();
                }else{
                    write = write + ", " + lerArq.next();
                }
                
            }
            ArrayText.setText(write);
        }catch(Exception e){
            System.out.println("Erro: " + e);
            JOptionPane.showMessageDialog(null, "Erro " + e, "Fatal Error", JOptionPane.ERROR_MESSAGE);
            dispose();
        }
    }
    
